class Calculator
{
public static void main(String[] args)
{
Calculator calc= new Calculator();
calc.add();
calc.Subraction();
calc.Multiply();
calc.Divide();

}
void add()
{
System.out.println(10+30);
}
void Subraction()
{
System.out.println(100-40);
}
void Multiply()
{
System.out.println(10*10);
}
void Divide()
{
System.out.println(2/50);
}
}